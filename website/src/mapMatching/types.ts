type GeoApifyLegStep = {
    osm_way_id: number;
    speed: number;
    speed_limit: number;
    surface: string;
    lane_count: number;
    road_class: string;
    from_index: number;
    to_index: number;
    name?: string;
    toll: boolean;
    tunnel: boolean;
    bridge: boolean;
    roundabout: boolean;
    traversability: string;
    time: number;
    distance: number;
    begin_bearing: number;
    end_bearing: number;
};

type GeoApifyWayPoint = {
    original_index: number;
    location: [number, number];
    original_location: [number, number];
    match_type: 'matched' | 'unmatched' | 'interpolated';
    match_distance: number;
    leg_index: number;
    step_index: number;
};

export interface GeoApifyMapMatchingResult {
    type: string;
    features: {
        type: string;
        properties: {
            distance: number;
            time: number;
            legs: {
                time: number;
                distance: number;
                steps: GeoApifyLegStep[];
            }[];
            mode: string;
            waypoints: GeoApifyWayPoint[];
        };
        geometry: { type: string; coordinates: [[number, number][]] };
    }[];
}

